let updated = 0;

for (const [sessionId, { summary, body }] of Object.entries(SUMMARIES)) {
  const filePath = join(sessionsDir, `${sessionId}.md`);
  let content;
  try {
    content = readFileSync(filePath, "utf8");
  } catch {
    console.warn(`SKIP (not found): ${sessionId}.md`);
    continue;
  }

  // 1. Replace summary field in frontmatter
  content = content.replace(
    /^summary:\s*.+$/m,
    `summary: "${summary.replace(/"/g, '\\"')}"`
  );

  // 2. Add campaign tag if missing
  const campaign = CAMPAIGN_TAGS[sessionId];
  if (campaign && !content.includes(`"${campaign}"`)) {
    content = content.replace(
      /^(tags:\n(?:\s+-\s+.+\n)*)(\s+-\s+"session")/m,
      `$1  - "${campaign}"\n$2`
    );
  }

  // 3. Replace ## 요약 section body (keep heading, replace content until next ##)
  const summarySection = `## 요약\n\n${body}\n`;
  content = content.replace(
    /## 요약\n\n[\s\S]*?(?=\n## )/,
    summarySection + "\n"
  );

  writeFileSync(filePath, content, "utf8");
  console.log(`  updated: ${sessionId}`);
  updated++;
}

console.log(`\nDone. Updated ${updated} session files.`);
